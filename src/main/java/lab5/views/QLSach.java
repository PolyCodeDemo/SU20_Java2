    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        String tenSach = this.txtTenSach.getText();
        String tacGia = this.txtTacGia.getText();
        String theLoai = this.cbTheLoai.getSelectedItem().toString();
        String ngayXB = this.txtNgayXB.getText();
        
        boolean kiemTra = this.kiemTraForm(tenSach, theLoai, tacGia, ngayXB);
        if (kiemTra == false) {
            return ;
        }
        
        DefaultTableModel model = (DefaultTableModel)this.tblSach.getModel();
        model.addRow(new Object[] {tenSach, theLoai, tacGia, ngayXB});
        try {
            Date ngay = this.parseDate(ngayXB);
            listSach.add(new Sach(tenSach, tacGia, theLoai, ngay));
        } catch (ParseException ex) {
            Logger.getLogger(QLSach.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        this.clearForm();
    }//GEN-LAST:event_btnThemActionPerformed


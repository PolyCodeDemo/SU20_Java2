    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int row = this.tblSach.getSelectedRow();
        
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Chọn 1 dòng dữ liệu để update!");
            return ;
        }
        
        String tenSach = this.txtTenSach.getText();
        String tacGia = this.txtTacGia.getText();
        String ngayXB = this.txtNgayXB.getText();
        String theLoai = this.cbTheLoai.getSelectedItem().toString();
        boolean kiemTra = this.kiemTraForm(tenSach, theLoai, tacGia, ngayXB);
        if (kiemTra == false) {
            return ;
        }
        
        Date ngay = new Date();
        try {
            ngay = this.parseDate(ngayXB);
        } catch (ParseException ex) {
            Logger.getLogger(QLSach.class.getName()).log(Level.SEVERE, null, ex);
        }
 
        Sach sach = this.listSach.get(row);
        sach.setNgayXB(ngay);
        sach.setTenSach(tenSach);
        sach.setTenTacGia(tacGia);
        sach.setTheLoai(theLoai);
        
        this.tblSach.setValueAt(tenSach, row, 0);
        this.tblSach.setValueAt(theLoai, row, 1);
        this.tblSach.setValueAt(tacGia, row, 2);
        this.tblSach.setValueAt(ngayXB, row, 3);
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int row = this.tblSach.getSelectedRow();
        
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Chọn 1 dòng để xoá!");
            
            return ;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có muốn xoá ko?");
        System.out.println(confirm);

        // Người dùng click Yes
        if (confirm == 0) {
            this.listSach.remove(row);
            DefaultTableModel model = (DefaultTableModel)this.tblSach.getModel();
            model.removeRow(row);
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnLuuFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuFileActionPerformed
        // TODO add your handling code here:
        String filePath = "/Users/tiennh/Desktop/output.txt";
        try {
            FileWriter fileWriter = new FileWriter(filePath, false);
            BufferedWriter bufferedWriter = new BufferedWriter(fileWriter);

            for (int index = 0; index < this.listSach.size(); index++) {
                Sach sach = this.listSach.get(index);
                String text = sach.getTenSach() + "," + sach.getTheLoai() + "," +
                    sach.getTenTacGia() + "," + sach.getNgayXB().toString();

                // Ghi vào file
                bufferedWriter.write(text);
                bufferedWriter.newLine();
            }

            bufferedWriter.flush();
            bufferedWriter.close();
            fileWriter.close();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Có lỗi xảy ra, vui lòng thử lại sau!");
        }
    }//GEN-LAST:event_btnLuuFileActionPerformed

    private void btnMoFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMoFileActionPerformed
        // TODO add your handling code here:
        if (this.listSach.size() != 0) {
            int confirm = JOptionPane.showConfirmDialog(this, "Dữ liệu trong bảng có thể chưa được lưu. Bạn có muốn lưu lại không?");
            
            if (confirm == 0) {
                // Lưu dữ liệu ở bảng vào file.
            }
        }
        
        String filePath = "/Users/tiennh/Desktop/input.txt";
        this.listSach.clear();
        DefaultTableModel model = (DefaultTableModel)this.tblSach.getModel();

        try {
            FileReader fileReader = new FileReader(filePath);
            BufferedReader bufferedReader = new BufferedReader(fileReader);
            
            String line;
            while ((line = bufferedReader.readLine()) != null) {
                String[] data = line.split(",");
                Date ngayXB = this.parseDate(data[3]);
                Sach sach = new Sach(data[0], data[2], data[1], ngayXB);
                this.listSach.add(sach);
                model.addRow(data);
            }

        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Có lỗi xảy ra, vui lòng thử lại sau!");
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Có lỗi xảy ra, vui lòng thử lại sau!");
        } catch (ParseException ex) {
            Logger.getLogger(QLSach.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnMoFileActionPerformed

    protected boolean kiemTraForm(
        String tenSach,
        String theLoai,
        String tacGia,
        String ngayXB
    ) {
        if (
            tenSach.length() == 0 ||
            theLoai.length() == 0 ||
            tacGia.length() == 0 ||
            ngayXB.length() == 0
        ) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin!");
            return false;
        }
        
        SimpleDateFormat formatter = new SimpleDateFormat();
        formatter.applyPattern("dd-MM-YYYY");
        
        try {
            Date date = formatter.parse(ngayXB);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Ngày tháng không đúng định dạng!");

            return false;
        }

        return true;
    }
    
    protected Date parseDate(String str) throws ParseException
    {
        SimpleDateFormat formatter = new SimpleDateFormat();
        formatter.applyPattern("dd-MM-YYYY");
        Date date;
        try {
            date = formatter.parse(str);
        } catch (ParseException ex) {
            throw ex;
        }
        
        return date;
    }
    
